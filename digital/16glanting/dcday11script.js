
/* variable to switch images */
var butcherGif=new Image()
butcherGif.src="assets/butcher.gif"

var butcherJpeg=new Image()
butcherJpeg.src="assets/butcher_A.gif"

/* variable that stores if the image has been clicked */
var butcherClicked = false;

/* function that swaps the first image for the second image */
function butcher() {
	if (butcherClicked == false) {
		document.getElementById('butcherGo').src=butcherGif.src;
		butcherClicked = true;
	} else {
		document.getElementById('butcherGo').src=butcherJpeg.src;
		butcherClicked = false;
	}
}


